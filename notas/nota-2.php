<p>El jueves 25 en horas de la noche y sábado 27 al promediar el mediodía, el viceministro en compañía del Director General del hospital, Dr. José Roca Mendoza; y el Director Adjunto, Dr. Manuel Silva; se desplazó por los principales ambientes del nosocomio, verificando la atención que se brinda a los pacientes que aún permanecen hospitalizados debido a la gravedad de sus heridas.</p>

<p>Por su parte, el director del nosocomio expresó que los heridos de los sucesos de los días jueves 25 y sábado 27 vienen recuperándose favorablemente y cuentan con el apoyo de especialistas capacitados. “Los gastos de la atención, tratamiento y medicamentos son cubiertos por el Seguro Integral de Salud (SIS) en aquellos pacientes plenamente identificados”, indicó el Dr. José Roca Mendoza.</p>

<p>Hasta el 30 de octubre, de acuerdo al último reporte del hospital, un total de 18 personas, entre ellas dos menores de edad, permanecen internadas. El resto fueron dados de altas en los últimos días.</p>

<p>Cabe destacar que debido a estos incidentes, se reportaron cuatro fallecimientos y un centenar de heridos que fueron atendidos en el Hospital Nacional Dos de Mayo.</p>
