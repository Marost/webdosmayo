<p>Demostrando que el Hospital Nacional “Dos de Mayo” cuenta con equipamiento de última generación, posee el angiografo digital biplanar más moderno del Perú, el cual es único en el país y tercero en Sudamérica.</p>

<p>Permite efectuar evaluaciones diagnósticas y terapéuticas así como la reconstrucción tridimensional (3D) de las arterias y cavidades del corazón y miembros superiores e inferiores, en niños y adultos.</p>

<p>Mediante este equipo de última tecnología, valorizado en más de 4 millones de dólares, se efectúa una vista radiográfica, detectando dolencias como estenosis coronarias, trombosis periféricas, aneurismas cerebrales, comunicaciones interauriculares e interventriculares, defectos cardíacos congénitos, malformaciones vasculares y angioplastias.</p>

