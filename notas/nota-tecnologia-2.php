<p>Hasta la fecha, un total de 392 pacientes, que se tratan en el nosocomio y otros establecimientos, han utilizado este equipo de última tecnología.</p>

<p>El angiografo, de marca PHILIPS y modelo Allura FD 1020, está compuesto por un angiografo biplanar (de dos arcos en C con sus respectivos Rayos X), seis monitores para la visualización de imágenes, una bomba inyectora de medio de contraste, un polígrafo de registro de presiones, una maquina de anestesia y una máquina de electrocauterio.</p>

<p>Asimismo, consta de un cardio defibrilador, un monitor de presión, una banda térmica y una estación de trabajo con cinco monitores.</p>

<p>Bajo el manejo y responsabilidad de este equipo se encuentra un equipo multidisciplinario altamente calificado. Se ubica en el sótano del Centro de Tratamiento Intensivo y Diagnóstico Especializado (CETIDE).</p>
