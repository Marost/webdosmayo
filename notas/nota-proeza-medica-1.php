                                <h2>CIRUJANOS DEL HOSPITAL DOS DE MAYO RECONSTRUIRÁN ROSTRO DE MADRE AREQUIPEÑA HERIDA EN ACCIDENTE DE TRÁNSITO</h2>
                            </div>

